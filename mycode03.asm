.MODEL SMALL
.STACK 100H
.DATA
RESULT DB 0 
RESULT_N DB 0
MLT DB 1
CC DB 0
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX   

PR:
 MOV DL,RESULT
 MOV RESULT_N,DL
 JP LP 

OLP:
LP:
 MOV AH,1
 INT 21H
 CMP AL,13
 JE EXIT
 
 SUB AL,48
 MOV CL,AL

 MOV AL,RESULT
 MOV BL,10
 MUL BL
 ADD AL,CL
 MOV RESULT,AL
 
 JMP LP  
EXIT: 
 INC CC
 CMP CC,2
 JL PR
                              
 MOV CL,RESULT
 ADD CL,RESULT_N
 MOV AH,2
 MOV DL,CL
 INT 21H 

 MAIN ENDP
END MAIN   


